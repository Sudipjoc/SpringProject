package abstraction;

public class AbstractionDocs {
	
	/*
	 *  
	 *  ======= Abstraction ==========
	 *   #process of hiding implementation details in the program
	 *   
	 *   # To achieve abstraction :
	 *      a> Abstract class :
	 *          #class which contains abstract(un-implemented methods) and non-abstract(implemented method) methods.\
	 *          
	 *          #Abstract Method :
	 *            Methods which does not have implementation details or body is called abstract method.
	 *            e.g.
	 *            abstract void sum();
	 *       syntax Abstract class :
	 *       abstract class class-name{
	 *       			//abstract methods
	 *       			//non abstract methods
	 *       }
	 *       
	 *       # we can't create object of the abstract class.
	 *       # every abstract class must have at least one child class and child class must
	 *       override abstract methods.
	 *       #level of abstraction (0-100)
	 *      
	 *      
	 *      b> Interface :
	 *      
	 *        # Same as class which contains public abstract methods and
	 *        public final variables only.
	 *        
	 *        # We can't create object of interface
	 *        # Every interface must have at least one implementation class
	 *        and implementation class must override abstract methods.
	 *        # Level of abstraction 100%.  
	 *          
	 *           Syntax:
	 *           interface interface-name{
	 *           //public static final variables
	 *           //public abstract methods
	 *           }		 
	 *  
	 */
	
	
	

}
