

package corejava3pm;


public class TypeCasting {

	
	
	/*
	 *  =========== Type Casting ==========
	 *  
	 *  Syntax :
	 *  
	 *  data_type var = (data_type) value ;
	 *  
	 *  1.implicit casting (auto casting)
	 *  
	 *  byte->short -> int -> long -> float -> double
	 *  
	 *  
	 *  
	 *  2. explicit casting
	 * double - > float - > long -> int - > short -> byte -> char
	 * 
	 * 
	 */


	public static void main(String[] args) {
		
		//implicit casting
		
		byte k =123;
		int p = k;
		
		//explicit casting
		
		double m =3564545.4534;
		int n = (int) m;
		
	}
	
}



